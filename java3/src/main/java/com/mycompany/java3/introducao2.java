package com.mycompany.java3;

public class introducao2 {
    public static void main (String[] args) {
        // Declaração de variáveis
        int a = 10;
        int b = 2;
        int maior;
        
        // Operadores aritméticos
        if (a > b) {
            maior = a;
        } else {
            maior = b;
        }
        
        // Exibindo resultados 
        System.out.println("O maior número é: " + maior);
    }
    
}
